      <p class="my-4">
        <strong class="text-xl"
          >Recruiters can be really valuable to work with as a job hunter.
        </strong>
      </p>

      <p class="my-4">
        Recruiters are actively trying to fill jobs. It is their job to know
        what the company is looking for and recommend a number of qualified
        applicants. Resumes should highlight your technical experience but can
        include your former career. This gives employers a sense both that you
        can do the technical work and also a sense of who you are. In an
        employment market that increasingly interested in cultural fit, who you
        are matters. As I interpreted that advice, I envision filling the top
        half of my resume with technical projects but also including the basics
        of the work that I have done leading up to the career shift.
      </p>

      <p class="my-4">
        <strong class="text-xl"
          >Objectives or a short cover letter can help contextualize a career
          change.
        </strong>
      </p>

      <p class="my-4">
        Melinda called out that including an objective could be perceived as a
        little &ldquo;old school&rdquo;. From her perspective, it also provides
        context for why someone with a perfectly good career in higher ed might
        up and decide to become a back end web developer. . . for example. The
        more relevant technical skills you can highlight the better. The skills
        can come from previous work, your capstone, a startup weekend, an
        internship, volunteering, or anything else that allows you to
        demonstrate passion for and experience with programming. Every little
        bit helps. It is worth highlighting the little experiences until you
        have bigger ones to highlight.
      </p>

      <p class="my-4">
        <strong class="text-xl"
          >Every recruiter and HR person is human and different.
        </strong>
      </p>

