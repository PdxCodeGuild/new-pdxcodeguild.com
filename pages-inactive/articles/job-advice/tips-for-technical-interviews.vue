    <article class="mx-6 sm:mx-24 lg:mx-48">
      <!-- byline -->
      <p class="text-right mt-8">by Sheri Dover - Sep. 10, 2019</p>
      <!-- title -->
      <h1 class="font-bold text-5xl">
        Cracking the Coding Interview: 6 Tips for Technical Interviews
      </h1>
      <!-- body -->

      <p class="my-4">
        Congratulations!&nbsp; You&rsquo;ve landed that in-person interview!
        Tackling your first interview can be less nerve-racking when you know
        what to expect.&nbsp;&nbsp;
      </p>

      <p class="my-4">
        It the programming world, each technical interview is unique, and may
        include anything from whiteboarding, knowledge-based questions and brain
        teasers designed to test your technical knowledge and how you work under
        pressure. The good news is that most technical interviews are designed
        to give you partial credit. Make sure you get all the credit you can by
        keeping in mind the following tips designed to help you ace your next
        technical interview.&nbsp;
      </p>

      <h3 class="text-2xl my-4">
        <strong class="text-xl">How to prepare</strong>
      </h3>

      <ol>
        <li>
          <p class="my-4">
            <strong class="text-xl">Practice Coding Challenges</strong>&nbsp;
          </p>
        </li>
      </ol>

      <p class="my-4">
        Spend a structured part of each day during your job search building your
        coding skills. Learn new skills by making web apps and building stuff
        rather than treading water in endless tutorials.&nbsp;
      </p>

      <p class="my-4">
        <strong class="text-xl"
          >2. Use Glassdoor.com to research the company.</strong
        >
      </p>

      <p class="my-4">
        Find out what they have asked in interviews in the past, what
        technologies they use, what they value and what the normal compensation
        is for the position you are applying for.&nbsp;
      </p>

      <h3 class="text-2xl my-4">
        <strong class="text-xl">What to expect</strong>
      </h3>

      <p class="my-4">
        Most tech companies start with a phone screening. They will ask
        questions about your skills and experience, get a general sense of who
        you are behaviorally and ask about your compensation expectations. Do
        not give the first number! Tell them that at this point you are most
        interested in finding out how you fit in the company and how you can add
        value to a team there. They will ask you again right before they offer
        you a job. At that point, you might tell them that you don&rsquo;t have
        that number at this time and that it will partially depend on the
        benefits package. You may also say that &ldquo;I&rsquo;m looking for the
        normal market rate for the position&rdquo;.&nbsp;
      </p>

      <p class="my-4">
        The next step is usually an off-site tech challenge. The company will
        send you one or more tech challenges or short projects to solve in a
        given time frame. It&rsquo;s perfectly acceptable to ask questions about
        what to expect in the challenge before you start. It shows that you
        think ahead. Again, reading about the company&rsquo;s interview process
        on Glassdoor can help.&nbsp;
      </p>

      <p class="my-4">
        After that, you will do in in-house tech interview, or an in-house
        behavioral interview.&nbsp;
      </p>

      <h3 class="text-2xl my-4">Tips for an in-house technical interview</h3>

      <ol>
        <li>
          <p class="my-4">
            <strong class="text-xl"
              >Show them how you solve problems by talking through what you
              don&rsquo;t know.&nbsp;</strong
            >
          </p>
        </li>
      </ol>

      <p class="my-4">
        Technical interviews are designed to find out what you know, how you
        solve problems and how you work under pressure. It&rsquo;s completely
        okay and normal to not know the answer to everything. You probably know
        something related to the question. If you don&rsquo;t know an answer,
        talk about what you do know about the topic. Show what you know by
        keeping the dialog going. Keep calm by practicing your breathing,
        ensuring that you&rsquo;re maintaining an even tempo. Whenever you feel
        the nerves start to get to you, smile like you mean it! When you smile,
        it tends to put your interviewer at ease which in turn will help you to
        feel at ease too.&nbsp;
      </p>

      <p class="my-4">
        The questions and problems you&rsquo;ll be presented with will
        inevitably get progressively harder in order to test where your
        knowledge starts and stops. Talking through your thought process and
        writing out your work are sure-fire ways to keep a calm head.&nbsp;
      </p>

      <p class="my-4">
        <strong class="text-xl"
          >2. It&rsquo;s okay to be nervous - most everyone is nervous during an
          interview!</strong
        >
      </p>

      <p class="my-4">
        If you get stuck, take a breath and let your interviewer know that
        you&rsquo;re nervous. It helps fill in what could otherwise be silence
        and reminds the interviewer that you probably know more than
        you&rsquo;re able to say.&nbsp;&nbsp;
      </p>

      <p class="my-4">
        <strong class="text-xl"
          >3. Don&rsquo;t forget your interviewer. You may be looking at your
          future teammate!</strong
        >
      </p>

      <p class="my-4">
        Your interviewer is looking for skilled communicators and collaborators.
        If you need clarification, ask! This opens up a dialogue with the
        interviewer while gaining context if you need a little assistance.
        Don&rsquo;t discount your interviewer- they&rsquo;re an important
        resource!&nbsp;
      </p>

      <p class="my-4">
        When all is said and done, your interviewer is someone who you may be
        working with every day. Even if you missed a few questions, the overall
        connection that you make should be positive as they are looking for
        someone who can elevate their team. If you&rsquo;re anxious about small
        talk, a good tip is to practice a 30 second elevator pitch for when
        you&rsquo;re asked to talk about yourself at home.
      </p>

      <p class="my-4">
        <strong class="text-xl"
          >4. Don&rsquo;t give up and pass on an answer.
        </strong>
      </p>

      <p class="my-4">
        When in doubt, use pseudo-code, or talk through what you do know.&nbsp;
      </p>

      <p class="my-4">
        It&rsquo;s better to demonstrate that you understand the underlying
        logic - even if you can&rsquo;t remember the syntax - than it is to
        leave the question on the table. Practicing how you will handle getting
        stumped is a good way to ensure you prevail should it actually happen.
        Saying, &ldquo;I&rsquo;m not sure but drawing from my knowledge of these
        related topics I would guess the answer may be&hellip;&rdquo; is a much
        better way of answering interview questions than just saying, &ldquo;I
        don&rsquo;t know.&rdquo; Mastering this form of answering demonstrates
        that you are thinking creatively outside the box and drawing on your
        knowledge and that you&rsquo;re not content with giving up. Interviewers
        are looking for candidates who show drive, grit, and initiative like
        this.&nbsp;
      </p>

      <p class="my-4">
        <strong class="text-xl"
          >5. Leave arrogance at the door, but still get a leg up by allowing
          the interviewer to see you as a rockstar for multiple
          projects.&nbsp;</strong
        >
      </p>

      <p class="my-4">
        Top candidates are eager to learn, adaptable, and resilient to the
        ever-changing environment of technology and business needs succeed. The
        humbler and more eager to learn you appear, the more projects and roles
        your future manager can envision you in.&nbsp;
      </p>

      <p class="my-4">
        When interviewing, make sure to approach the situation as if there is
        much to be learned during that interview and your interviewer will
        remember you as someone they can teach, grow, and develop into a team
        superstar in the future.
      </p>

      <p class="my-4">
        <strong class="text-xl">6. Brag just a little. </strong>
      </p>

      <p class="my-4">
        Show them what you&rsquo;re made of by showcasing some recent projects!
      </p>

      <p class="my-4">
        While you don&rsquo;t want to be arrogant, you do want to show off your
        skills! Come prepared with one or two projects that you accomplished in
        your last position, or on your own! Showcase a project that has the
        minimum level of complexity to match the job you&rsquo;re applying
        to.&nbsp;
      </p>

      <p class="my-4">
        Don&rsquo;t have a project you can showcase? Try building one! Study the
        job requirements and produce an independent project that matches in
        complexity. The extra level of detail, work, and effort will set you
        apart and give you a leg up before you even walk in the door!
      </p>

