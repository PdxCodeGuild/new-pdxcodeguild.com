      <h2 class="text-3xl my-8">
        Heard mixed reviews? Deciding if this path is for you? This will clear
        it right up.<br />
        <br />
        What is a Coding Bootcamp?
      </h2>

      <p class="my-4">
        A coding bootcamp is an intensive, technical program designed to teach
        programming skills that employers are looking for in a short amount of
        time. Coding boot camps are often attended by those with very little
        coding background who are interested in a career in tech. Tech jobs are
        in high demand, and extremely well paid, ranking in 8 of the top 15 jobs
        according to Glassdoor. After attending a good boot camp, students are
        equipped to apply for junior developer positions with a U.S. average
        salary of around $67,000 per year.
      </p>

      <p>&nbsp;</p>

      <h2 class="text-3xl my-8">Are All Bootcamps Created Equal?</h2>

      <h3 class="text-2xl my-4">
        What difference does it make how I learn to code?
      </h3>

      <p>&nbsp;</p>

      <p class="my-4">
        The three biggest differences between a programmer bootcamp and any
        other mode of learning to code are these&hellip;
      </p>

      <p class="my-4">1. Quality of programming skills gained</p>

      <p class="my-4">
        2. The amount of earning potential over the next few years
      </p>

      <p class="my-4">
        3. Your chances of finishing Bootcamps specialize in getting students
        skills fast so they can start high-paying careers in as little as 12
        weeks. Learning by other routes has an opportunity cost of missed wages
        and lost job experience while you are taking a longer, and often lest
        complete route to learn. Many students overestimate their
        self-discipline in self-paced programs and there&rsquo;s a much higher
        drop out rate.
      </p>

      <p>&nbsp;</p>

      <p class="my-4">
        <strong class="text-xl">Online bootcamps</strong>&nbsp;
      </p>

      <p class="my-4">
        There are two times online learning is most useful. It&rsquo;s great
        when someone is dabbling or learning what they can in preparation for a
        more robust course of learning, and when a programmer needs to brush up
        on some new syntax. Online courses will teach you mostly Syntax (the
        small differences in how each programming language is written) and
        you&rsquo;re going to learn how the code is written, but you&rsquo;re
        also going to have major gaps in important foundations. Working in teams
        simulates real work experience, problem solving under intense deadlines
        will also prepare you to manage the pressure that comes in this
        industry. We see so many students from online programs who take their
        time in self-paced programs, redoing the same problems over and over
        rather than adding to their stack. Many online bootcamps also make big
        promises, but be weary of ones that have &ldquo;guaranteed job
        placement&rdquo; or a &ldquo;money back guarantee.&rdquo; If it sounds
        too good to be true, it usually is. While these are enticing offers,
        many of these guaranteed jobs are low paying and not expanding your
        skill set the way you&rsquo;re capable of. This will give you a platform
        to lead into tech, but for how much money you will spend and the amount
        of time it takes to get where you want to be may not be what you were
        expecting.
      </p>

      <p class="my-4">
        <strong class="text-xl">Physical bootcamps</strong>&nbsp;
      </p>

      <p class="my-4">
        In the location you will be working from are often most recommended as
        there are many networking benefits by taking this route. A good
        programmer bootcamp like PDX Code Guild will have a strong, up-to-date
        curriculum taught in small groups by instructors with recent industry
        experience.
      </p>

      <p class="my-4">
        The curriculum will focus on the individual student solving coding labs
        mixed with team and pair projects. Knowledgeable instructors should be
        able to keep each student challenged so that each student gets as much
        out of the bootcamp as possible and never has to wait for the rest of
        the class to catch up to them. It should stay at an intense learning
        curve most of the time.
      </p>

      <p>&nbsp;</p>

      <h2 class="text-3xl my-8">What Happens After the Camp Is Over?</h2>

      <h3 class="text-2xl my-4">
        How much can I make and what kinds of jobs can I get?
      </h3>

      <p class="my-4">
        If you take a programmer bootcamp, you can have skills for a high-paying
        job in 12-24 months. In Portland, PDX Code Guild graduates make between
        $45,000 and $95,000 in their first year on the job. Each year they work
        as a programmer, their earning power goes up. After two or three years
        in the field, a backend developer can make between $90,000 and $120,000
        per year in Portland. With such a high demand for quality programmers,
        almost every graduate who wants a job will find one. Your interviewing
        skills and ability to articulate your knowledge can greatly affect the
        range in offers you receive, and many times it takes 3-4 months to find
        the right job. The jobs are really a mixed bag. You will be well
        situated for a job in web development. Lots of people end up in engineer
        positions where they help clients install integrate software into their
        products. Lots go into dev ops, deployment, taking care of servers,
        software testing, Q&amp;A positions, and IOT.
      </p>

      <p>&nbsp;</p>

      <h2 class="text-3xl my-8">
        Computer Science Degree vs Programmer Bootcamp
      </h2>

      <h3 class="text-2xl my-4">What&rsquo;s the difference?</h3>

      <p class="my-4">
        If you were to go to university instead of a bootcamp, you may lose out
        on a significant amount of money. For the four &ndash; five years you
        would be in college, you may miss out on between $245,000 and $435,000
        plus what you pay for tuition and student loan interest. University CS
        degree programs were designed to train researchers and not programmers.
        They tend to focus on theory and most CS degree graduates only take one
        semester of actual programming.
      </p>

      <p class="my-4">
        At PDX Code Guild, we commonly hear from graduates of CS programs
        complain that they know a little bit of code, but don&rsquo;t know how
        to start a project, and don&rsquo;t know how to do all the bits and
        pieces you need to work as a programmer. Most bootcamp students already
        have a college degree, but if you don&rsquo;t, and you want to get one,
        you may consider going to a bootcamp, working as a programmer, save your
        money for school and go later without having to take out huge loans.
        You&rsquo;ll save a significant amount of interest and you may even be
        able to get your employer to pay for some of it.
      </p>

      <p>&nbsp;</p>

      <p class="my-4">
        <strong class="text-xl">Community College&nbsp;</strong>
      </p>

      <p class="my-4">
        Community college classes move slowly. You can learn in one week at
        bootcamp what will take you a whole term in college (community or
        university). At a community college you learn job skills piece by little
        piece and it can take a couple years to gain entry-level job skills.
        It&rsquo;s unlikely that you will get as strong as programming skills at
        a community college as you would in a good 12-week programmer bootcamp.
        Most technical classes at community colleges are designed to be
        transferred to a 4-year CS degree program or are for web development. In
        general you wont build the skills or portfolio you need for the
        higher-paying jobs at a community college, but if you find a good one,
        you will still lose approximately $100,000 - $165,000 in lost wages in
        the year and a half that you were studying longer than you would have
        been in a bootcamp.
      </p>

      <p>&nbsp;</p>

      <h2 class="text-3xl my-8">Who Is It NOT Right For?</h2>

      <p class="my-4">
        If you don&rsquo;t like to program, this would be a bad career choice.
        If you are easily frustrated by puzzles, if you don&rsquo;t like to
        constantly learn new things, again not the best fit. Succeeding at a
        good bootcamp takes grit, determination and intrinsic motivation.
        Bootcamp is a very active learning environment and is best suited to
        hard-working, highly motivated students who can manage frustration, give
        themselves permission to make mistakes, and keep going. Bootcamp
        students who posses these qualities are rewarded with high-paying jobs
        in a field with excellent work conditions and often phenomenal benefits.
      </p>

      <p>&nbsp;</p>

      <h2 class="text-3xl my-8">
        Choosing a bootcamp Bootcamps vary in quality.
      </h2>

      <p class="my-4">
        Look for bootcamps that are licensed. Avoid bootcamps with too much pair
        programming, classes led by teaching assistants or graduates of their
        own program with no experience working as a programmer, lectures given
        by video or have large class sizes. These programs tend to be more
        focused on making a buck than quality bootcamp education.
      </p>

